class Solution:
    def combine(self, n: int, k: int) -> List[List[int]]:
        res, path = [], []
        def dfs(start):
            if len(path) == k:
                res.append(path[:])
                return
            need = k - len(path)
            for x in range(start, n - need + 2):
                path.append(x)
                dfs(x + 1)
                path.pop()
        dfs(1)
        return res
        